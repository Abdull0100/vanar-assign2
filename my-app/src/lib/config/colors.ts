// Helper function to get current theme colors
export function getTheme() {
  return currentTheme;
}

// Helper function to get gradient classes
export function getGradient(from: string, to: string) {
  return `bg-gradient-to-br ${from} ${to}`;
}

// Helper function to get button classes
export function getButtonClasses(variant: 'primary' | 'secondary' | 'success' | 'error' = 'primary') {
  const theme = getTheme();
  const colors = theme[variant];
  
  return `bg-gradient-to-r ${colors.dark} ${colors.darker} text-white font-semibold py-3 px-6 rounded-xl shadow-lg hover:shadow-xl transform hover:-translate-y-1 transition-all duration-200 ${colors.hover}`;
}
